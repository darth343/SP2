#ifndef CAMERA_5_H
#define CAMERA_5_H

#include "Camera.h"
/****************************************************************************/
/*!
Class Camera5:
\brief Defines a Dweller with name, SPECIAL, weapon, outfit and methods
*/
/****************************************************************************/
class Camera5 : public Camera
{
public:
	/****************************************************************************/
	/*!
	Default Position Vector
	*/
	/****************************************************************************/
	Vector3 defaultPosition;
	/****************************************************************************/
	/*!
	Default Target Vector
	*/
	/****************************************************************************/
	Vector3 defaultTarget;
	/****************************************************************************/
	/*!
	Default Up Vector
	*/
	/****************************************************************************/
	Vector3 defaultUp;
	Camera5();
	~Camera5();
	/****************************************************************************/
	/*!
	Value of Vertical Rotation of the Camera
	*/
	/****************************************************************************/
	float vertRot = 0;
	/****************************************************************************/
	/*!
	Value of the Horizontal Rotation fo the Camera
	*/
	/****************************************************************************/
	float horiRot = 0;
	/****************************************************************************/
	/*!
	Vector that stores view vector of camera
	*/
	/****************************************************************************/
	Vector3 view;
	/****************************************************************************/
	/*!
	Vector that stores right vector of camera
	*/
	/****************************************************************************/
	Vector3 right;
	/****************************************************************************/
	/*!
	Initializes camera values
	*/
	/****************************************************************************/
	virtual void Init(const Vector3& pos, const Vector3& target, const Vector3& up);
	/****************************************************************************/
	/*!
	Updates camera values
	*/
	/****************************************************************************/
	virtual void Update(double dt);
	/****************************************************************************/
	/*!
	Overloaded Camera Update Function
	*/
	/****************************************************************************/
	void Update(double dt, bool disableControls);
	/****************************************************************************/
	/*!
	Camera Reset Function
	*/
	/****************************************************************************/
	virtual void Reset();
};

#endif